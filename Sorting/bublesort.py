def bublesort(nums) -> None:
    n = len(nums)
    for i in range(n):
        is_sorted = True
        for j in range(0, n-i -1):
            if nums[j] > nums[j+1]:
                nums[j], nums[j+1] = nums[j+1], nums[j]
                is_sorted = False
        if is_sorted:
            return 

    return 

